///La temperatura esta disponible en unidades Fahrenheit, Celsius y Kelvin.
enum UnitsWeather {
  ///Para temperatura en Kelvin and velocidad del viento en metros/segundos
  standard,

  ///Para temperatura en Celsius y velocidad del viento en metros/segundos
  metric,

  ///Para temperatura en Fahrenheit y velocidad del viento en millas/hora
  imperial,
}
